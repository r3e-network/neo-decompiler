import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..", "..");
const sourcePath = resolve(repoRoot, "tools", "data", "syscalls.json");
const outputPath = resolve(repoRoot, "js", "src", "generated", "syscalls.js");

const syscalls = JSON.parse(readFileSync(sourcePath, "utf8")).sort(
  (left, right) => left.hash - right.hash,
);

mkdirSync(dirname(outputPath), { recursive: true });

writeFileSync(
  outputPath,
  `// This file is @generated by js/scripts/generate-syscalls.mjs. Do not edit manually.
export const SYSCALLS = new Map([
${syscalls
  .map(
    (entry) =>
      `  [0x${entry.hash.toString(16).padStart(8, "0").toUpperCase()}, ${JSON.stringify(entry)}],`,
  )
  .join("\n")}
]);
`,
);

console.log(`wrote ${outputPath}`);
